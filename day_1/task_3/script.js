// Task 3: Using operators in JavaScript

let a = 10;
let b = 20;

// Arithmetic Operators
console.log("The sum is:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus (remainder):", a % b);

// Comparison Operators
console.log("Equal (==):", a == b);           // Checks value only
console.log("Strict Equal (===):", a === b);  // Checks value and type
console.log("Not Equal (!=):", a != b);
console.log("Greater than (>):", a > b);

// Logical Operators
console.log("AND (&&):", a > 5 && b < 5);      // True if both are true
console.log("OR (||):", a > 5 || b > 5);       // True if at least one is true
console.log("NOT (!):", !(a < b));             // Reverses true/false
