// Task 5: Loops (for, while, do-while)

// For loop - print numbers 1 to 5
for (let i = 1; i <= 5; i++) {
  console.log("For loop:", i);
}

// While loop - print numbers 1 to 5
let j = 1;
while (j <= 5) {
  console.log("While loop:", j);
  j++;
}

// Do-while loop - runs at least once
let k = 1;
do {
  console.log("Do-while loop:", k);
  k++;
} while (k <= 5);
